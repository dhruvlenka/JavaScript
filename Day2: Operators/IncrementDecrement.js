//increment: post increment
var age = 24;
age++;
console.log(age);

//increment: pre increment
var time = 11;
++age;
console.log(time)

//decrement: post decrement
var batteryPercentage = 50;
batteryPercentage--;
console.log(batteryPercentage);

//increment: pre decrement
var batteryCharging = 85;
--batteryCharging;
console.log(batteryCharging);