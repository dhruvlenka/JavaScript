//arithmetic operators using var
var a = 10;
var b = 5;
console.log("Addition of " + a + "+" + b + " = "  + (a+b));
console.log("Substraction of " + a + "-" + b + " = "  + (a-b));
console.log("Multiplication of " + a + "*" + b + " = "  + (a*b));
console.log("Divide of " + a + "/" + b + " = "  + (a/b));
console.log("Modulo of " + a + "%" + b + " = "  + (a%b));
console.log("Increment of " + a + "++" + " = "  + (++a));
console.log("Decrement " + a + "--" + " = "  + (--a));
console.log("Power of " + a + "**" + b + " = "  + (a**b));